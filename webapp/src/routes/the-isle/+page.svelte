<div class="box">
    <h2>Auf der Insel heißt es fressen oder gefressen werden</h2>
    <p>The Isle ist ein ungewöhnliches Open-World-Survival-Horror Spiel, in dem Sie entweder einen Fleischfresser oder
        einen Pflanzenfresser spielen. Wähle aus mehreren Dinosauriern mit unterschiedlichen Stärken und Schwächen,
        starte als Jungtier, und versuche möglichst lange zu überleben. Deine Gegner sind andere Spieler und die
        ständige Suche nach Nahrung und Wasser. Kaufe dir "The Isle" jetzt für 19,99 € und erlebe das aktuell beste
        Dino-Survival-Spiel auf Steam.
    </p>
</div>

<div class="box">
    <h2>Überleben ist nicht einfach</h2>
    <p>Wie viele Survival-Spiele ist auch The Isle stolz auf seinen hohen Schwierigkeitsgrad und den Mangel an leicht
        verfügbaren Informationen. Wenn Sie das Spiel zum ersten Mal spielen, sollten Sie damit rechnen, dass Sie früh
        und möglicherweise oft sterben, während Sie lernen, was Sie tun müssen, um in einer Welt zu überleben, in der
        fast alles darauf aus ist, Sie zu töten. Die größte Herausforderung besteht oft einfach nur darin, Nahrung zu
        finden - wenn Sie sich in der Rolle eines fleischfressenden Dinosauriers befinden, stammt schließlich jede
        Mahlzeit von der Jagd. Wenn Sie die Frustration eines früh veröffentlichten Spiels ertragen können, werden Sie
        ein einzigartiges Spielerlebnis entdecken, das seine Dinosaurier-Charaktere in den Mittelpunkt stellt. Die
        liebevoll gerenderten Dschungel-Insel-Umgebungen und Dinosaurier-Modelle schaffen ein Spiel, das wie kein
        anderes aussieht.</p>
</div>

